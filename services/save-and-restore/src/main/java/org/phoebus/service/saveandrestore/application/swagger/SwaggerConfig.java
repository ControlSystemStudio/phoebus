package org.phoebus.service.saveandrestore.application.swagger;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import springfox.documentation.builders.RequestHandlerSelectors;
import springfox.documentation.spi.DocumentationType;
import springfox.documentation.spring.web.plugins.Docket;
import springfox.documentation.swagger2.annotations.EnableSwagger2;

@Configuration
@EnableSwagger2
public class SwaggerConfig {

	@Value("${hostname:localhost}")
	private String hostname;

	@Bean
	public Docket productApi() {
		return new Docket(DocumentationType.SWAGGER_2).host(hostname).select()
				.apis(RequestHandlerSelectors.basePackage("org.phoebus.service.saveandrestore")).build();

	}

}
