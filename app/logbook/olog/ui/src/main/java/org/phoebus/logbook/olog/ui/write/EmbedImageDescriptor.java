package org.phoebus.logbook.olog.ui.write;


import javafx.scene.image.Image;

/**
 * Simple wrapper around data needed to describe an embedded image object.
 */
public class EmbedImageDescriptor {

    /**
     * Image content.
     */
    private Image image;

    /**
     * The file name, either a file selected from the file system, or a constructed file name (UUID) if
     * the image is pasted from the clipboard.
     */
    private String fileName;

    /**
     * A unique id of the embedded image file.
     */
    private String id;
    private int width;
    private int height;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Image getImage() {
        return image;
    }

    public void setImage(Image image) {
        this.image = image;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
